              <span style="font-size:14px;">STEP</span>２.　歯周外科治療
            </p>
              <p class="margin-top">
                歯周基本治療をおこなっても、歯石が歯周ポケットの深いところに入り込んでいて除去できず、炎症が治らない場合には、外科的治療をおこなうことがあります。治療法にはフラップ手術、歯周組織再生療法、歯周形成外科手術があります。
              </p>


              <div class="margin-top basshi" style="margin-bottom: 0;">
                <p class="middle_title" style="border-bottom:1px solid #278CBC; margin-bottom:15px; font-weight:bold;">
                  フラップ手術
                </p>
                <p class="margin-top">
                  歯周ポケットが深い（６㎜以上）箇所の歯茎を部分麻酔してから切って開き、スケーラーの届かなかった部分の歯石や歯の根の表面の汚れを取り除きます。取り除いた後は、開いた歯茎をきちんと閉じて縫合します。抜糸は１週間程度でおこないます。
                </p>
              </div>

              <div class="margin-top basshi" style="margin-bottom: 0;">
                <p class="middle_title" style="border-bottom:1px solid #278CBC; margin-bottom:15px; font-weight:bold;">
                  歯周組織再生療法
                </p>
                <p class="margin-top">
                  通常の歯周治療では、失われた歯周組織を元通りの状態に戻すことはできません。歯周組織再生療法は、その歯周組織を元通りにする再生を期待する治療法です。<br>
                  エナメルマトリックスたんぱく質を主成分とした材料を用います。治療をおこなう歯茎を部分麻酔して開き、歯の根の表面にエナメルマトリックスたんぱく質を塗って新しい歯周組織ができてくるのを待つ方法です。使用材料によって、保険適用と自費治療があります。
                </p>
              </div>

              <div class="margin-top basshi" style="margin-bottom: 0;">
                <p class="middle_title" style="border-bottom:1px solid #278CBC; margin-bottom:15px; font-weight:bold;">
